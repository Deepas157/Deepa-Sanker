package RevBook3;

public class RevPro2 {
	int a;
	long l;
	short s1;
	byte b1;
	float f;
	double d;
	String s;
	char c;
	boolean b;
	public static void main(String[] args) {
		RevPro2 def=new RevPro2();
		System.out.println("primitive value of int is  "+def.a);
		System.out.println("primitive value of long is  "+def.l);
		System.out.println("primitive value of short is  "+def.s1);
		System.out.println("primitive value of byte is  "+def.b1);
		System.out.println("primitive value of float is  "+def.f);
		System.out.println("primitive value of double is  "+def.d);
		System.out.println("primitive value of String is  "+def.s);
		System.out.println("primitive value of char is  "+def.c);
		System.out.println("primitive value of boolean is  "+def.b);
	}
}
